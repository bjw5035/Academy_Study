package practice;

public class ForEx {
    public static void main(String[] args) {
        System.out.println();
        for(int i=1; i < 10; i++) {
            System.out.println("i = "+i);
        }

        for (int i=10; i > 0; i--) {
            System.out.println("i = "+i);
        }
    }
}
