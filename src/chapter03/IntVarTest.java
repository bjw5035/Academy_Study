package chapter03;
public class IntVarTest {
    public static void main(String[] args) {

        // 변수 선언
        int i0;
        long l0;

        // 변수 초기화
        i0 = 0;
        l0 = 0L;

        // 변수 선언과 초기화
        int i1 = 1;
        long l1 = 1L;

        // 출력 : 주의 => 변수명과 문자열은 다름
        System.out.print("i0");
        System.out.print("l0");
        System.out.print("i1");
        System.out.print("l1");

        System.out.println();

        System.out.print(i0);
        System.out.print(l0);
        System.out.print(i1);
        System.out.print(l1);
    }
}
