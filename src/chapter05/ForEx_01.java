package chapter05;

public class ForEx_01 {
    public static void main(String[] args) {

        for(int i=0; i < 10; i++) {
            System.out.println("i="+i);
        }

        int i = 0;
        while(i<10) {
            System.out.println("i = "+i);
            i++;
        }
    }
}
